package Classes;

/**
 *
 * @author Ross
 */
public class HTMLText {
    
    //Attributes
    private String tag;
    private String text;
    
    //Getters
    public String getTag()
    {
        return tag;
    }
    
    public String getText()
    {
        return text;
    }
    
    //Setters
    public void setTag(String tagIn)
    {
        tag = tagIn;
    }
    
    public void setText(String textIn)
    {
        text = textIn;
    }
}
